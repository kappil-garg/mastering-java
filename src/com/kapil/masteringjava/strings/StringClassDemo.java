package com.kapil.masteringjava.strings;

/**
 * Program to demonstrate basic usage of String Class in Java.
 * Demonstrates how to create and manipulate String objects.
 *
 * @author Kapil Garg
 */
public class StringClassDemo {

    public static void main(String[] args) {

        System.out.println("\n*** String Class Demo ***\n");

        String name = "Ted";
        System.out.println("Name :: " + name + " Mosby");

        String numberAsString = "29";
        System.out.println("Age :: " + numberAsString + " Years");

    }

}
