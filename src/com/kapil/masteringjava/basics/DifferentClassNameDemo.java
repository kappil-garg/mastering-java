package com.kapil.masteringjava.basics;

/**
 * Program to demonstrate source file name can differ from the class name in Java, except when the class is public.
 * If the class is declared public, the file name must match the class name exactly.
 *
 * @author Kapil Garg
 */
class DifferentClassName {

    public static void main(String[] args) {
        System.out.println("\nHello from Class :: DifferentClassName");
        System.out.println("Source File Name :: DifferentClassNameDemo.java");
    }

}
