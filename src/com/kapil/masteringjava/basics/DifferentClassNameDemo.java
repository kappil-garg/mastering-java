package com.kapil.masteringjava.basics;

/**
 * Program to demonstrate that the source file name can differ from the class name in Java.
 * Demonstrates that if the class is not public, the file name can be different.
 *
 * @author Kapil Garg
 */
class DifferentClassName {

    public static void main(String[] args) {
        System.out.println("\nHello from Class :: DifferentClassName");
        System.out.println("Source File Name :: DifferentClassNameDemo.java");
    }

}
